function cloneMockData(value) {
  return JSON.parse(JSON.stringify(value));
}

export function getMockPositionByIdentifier(identifier) {
  if (!identifier) return null;

  const position = MOCK_POSITION_CARDS.find(
    (item) =>
      item._id === identifier ||
      item.id === identifier ||
      item.slug === identifier,
  );

  return position ? cloneMockData(position) : null;
}

function normalizeMockApplication(position, item, index) {
  const fallbackName =
    item.applicant?.name || item.candidateName || `مرشح ${index + 1}`;
  const fallbackEmail =
    item.applicant?.email ||
    item.email ||
    `candidate${index + 1}.${position._id.replace("mock-position-", "")}@example.com`;

  const rating =
    item.rating ??
    (typeof item.score === "number"
      ? Math.max(1, Math.min(5, Math.round(item.score / 20)))
      : 0);

  const appliedAt = item.appliedAt || new Date().toISOString();

  return {
    _id: item._id || `mock-app-${position._id}-${index + 1}`,
    status: item.status || "new",
    rating,
    notes: item.notes || "",
    rejectionReason: item.rejectionReason || "",
    answers: Array.isArray(item.answers) ? item.answers : [],
    profileSnapshot: item.profileSnapshot || null,
    appliedAt,
    updatedAt: item.updatedAt || appliedAt,
    applicant: {
      _id: item.applicant?._id || `mock-applicant-${position._id}-${index + 1}`,
      name: fallbackName,
      email: fallbackEmail,
    },
    jobPosition: {
      _id: position._id,
      title: position.title,
      department: position.department,
      status: position.status,
      type: position.type,
      location: position.location,
    },
    isMock: true,
  };
}

export function getMockApplicationsByPositionIdentifier(identifier) {
  const position = getMockPositionByIdentifier(identifier);
  if (!position) return null;

  const applications = (position.applicationsData || []).map((item, index) =>
    normalizeMockApplication(position, item, index),
  );

  return applications.sort(
    (a, b) => new Date(b.appliedAt).getTime() - new Date(a.appliedAt).getTime(),
  );
}

export function getMockApplicationById(applicationId) {
  if (!applicationId) return null;

  for (const position of MOCK_POSITION_CARDS) {
    const list = (position.applicationsData || []).map((item, index) =>
      normalizeMockApplication(position, item, index),
    );

    const application = list.find((item) => item._id === applicationId);
    if (application) {
      return cloneMockData(application);
    }
  }

  return null;
}
